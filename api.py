# ---------------------------------------------------------------------------
# Recommender
# ---------------------------------------------------------------------------

class Recommender:
    def __init__(self):
        self.df:           pd.DataFrame | None = None
        self.Xs:           np.ndarray  | None = None
        self.Xw:           np.ndarray  | None = None
        self.scaler:       MinMaxScaler | None = None
        self.weights:      np.ndarray  | None = None
        self.rf:           RandomForestClassifier | None = None
        self.spots_lookup: dict = {}

    def load(self):
        logger.info("Loading engineered features from %s", ENG_CSV)
        eng = pd.read_csv(ENG_CSV)
        eng["noise_encoded"] = eng["noise_encoded"].fillna(eng["noise_encoded"].median())
        self.df = eng.reset_index(drop=True)

        X = eng[FEATURE_COLS].astype(float).values
        self.scaler = MinMaxScaler()
        self.Xs = self.scaler.fit_transform(X)

        logger.info("Training Random Forest for feature weighting...")
        self.rf = RandomForestClassifier(
            n_estimators=300, random_state=42, class_weight="balanced"
        )
        self.rf.fit(self.Xs, eng["category"])
        self.weights = self.rf.feature_importances_
        self.Xw = self.Xs * self.weights

        logger.info("Loading spot display data from %s", DATA_JSON)
        raw = json.loads(DATA_JSON.read_text(encoding="utf-8"))
        self.spots_lookup = {s["name"]: s for s in raw}

        logger.info("Recommender ready — %d spots loaded", len(self.df))
        return self

    def _build_query_vector(self, p: UserPrefs) -> np.ndarray:
        v = {
            "has_wifi":                float(p.wants_wifi),
            "has_outlets":             float(p.wants_outlets),
            "has_coffee":              float(p.wants_coffee),
            "has_food":                float(p.wants_food),
            "feat_quiet":              float(p.wants_quiet),
            "feat_outdoor":            float(p.wants_outdoor),
            "feat_comfortable_seating":float(p.wants_comfortable),
            "feat_shaded":             float(p.wants_outdoor),
            "feat_benches":            float(p.wants_outdoor),
            "feat_tables":             float(p.wants_group),
            "feat_group_friendly":     float(p.wants_group),
            "feat_printing_nearby":    0.0,
            "feat_natural_light":      float(p.wants_outdoor * 0.5),
            "weather_dependent":       float(p.weather_ok),
            "is_indoor":               float(1 - p.wants_outdoor),
            "is_outdoor":              float(p.wants_outdoor),
            "comfort_score":           float(p.wants_comfortable * 2 + p.wants_quiet),
            "connectivity_score":      float(p.connectivity_need),
            "group_score":             float(p.wants_group * 2),
            "outdoor_score":           float(p.wants_outdoor * 3),
            "amenity_score":           float(p.wants_wifi + p.wants_outlets
                                             + p.wants_coffee + p.wants_food),
            "noise_encoded":           float(p.noise_tolerance),
        }
        return np.array([v[c] for c in FEATURE_COLS])

    def recommend(self, prefs: UserPrefs) -> tuple[list[dict], dict]:
        qv  = self._build_query_vector(prefs)
        qvs = self.scaler.transform(qv.reshape(1, -1))
        qvw = qvs * self.weights

        sims    = cosine_similarity(qvw, self.Xw)[0]
        top_idx = np.argsort(sims)[::-1][: prefs.n_results]

        results = []
        for rank, idx in enumerate(top_idx, 1):
            name = self.df.iloc[idx]["name"]
            raw  = self.spots_lookup.get(name, {})
            results.append({
                "rank":        rank,
                "name":        name,
                "category":    self.df.iloc[idx]["category"],
                "similarity":  round(float(sims[idx]), 4),
                "description": raw.get("description"),
                "noise_level": raw.get("noise_level"),
                "features":    raw.get("features") or [],
                "best_times":  raw.get("best_times") or [],
                "avoid_times": raw.get("avoid_times") or [],
                "url":         raw.get("url"),
                "hours":       raw.get("hours"),
                "hours_note":  raw.get("hours_note"),
                "building":    raw.get("building"),
                "area":        raw.get("area"),
                "lat":         raw.get("lat"),
                "lng":         raw.get("lng"),
                "food_nearby": raw.get("food_nearby"),
                "access":      raw.get("access"),
            })

        query_vector = {
            FEATURE_LABELS[c]: round(float(qvs[0][i]), 3)
            for i, c in enumerate(FEATURE_COLS)
        }
        return results, query_vector

    def explain(self, spot_name: str, prefs: UserPrefs) -> list[dict]:
        matches = self.df[self.df["name"] == spot_name]
        if matches.empty:
            raise ValueError(f"Spot not found: {spot_name!r}")
        idx  = matches.index[0]
        qv   = self._build_query_vector(prefs)
        qvs  = self.scaler.transform(qv.reshape(1, -1))[0]
        spot = self.Xs[idx]

        rows = []
        for i, feat in enumerate(FEATURE_COLS):
            q_val   = float(qvs[i])
            s_val   = float(spot[i])
            weight  = float(self.weights[i])
            contrib = float(min(q_val, s_val) * weight)
            rows.append({
                "feature":      FEATURE_LABELS.get(feat, feat),
                "user_value":   round(q_val, 3),
                "spot_value":   round(s_val, 3),
                "rf_weight":    round(weight, 4),
                "contribution": round(contrib, 4),
            })
        return sorted(rows, key=lambda r: r["contribution"], reverse=True)

    def all_spots(self) -> list[dict]:
        return json.loads(DATA_JSON.read_text(encoding="utf-8"))


# ---------------------------------------------------------------------------
# Rate limiter — 60 requests/minute per IP
# ---------------------------------------------------------------------------

limiter = Limiter(key_func=get_remote_address, default_limits=["60/minute"])

# ---------------------------------------------------------------------------
# App + lifespan
# ---------------------------------------------------------------------------

rec = Recommender()

@asynccontextmanager
async def lifespan(app: FastAPI):
    rec.load()
    yield
    logger.info("Shutting down — total requests served: %d", _counters["total"])

app = FastAPI(
    title="UCLA Study Spot Recommender API",
    description=(
        "Content-based study spot recommender using RF-weighted KNN. "
        "Given user preferences, returns ranked UCLA study spots with "
        "similarity scores and per-feature explanations."
    ),
    version="1.0.0",
    lifespan=lifespan,
)

app.state.limiter = limiter
app.add_exception_handler(RateLimitExceeded, _rate_limit_exceeded_handler)

app.add_middleware(
    CORSMiddleware,
    allow_origins=["*"],
    allow_methods=["GET", "POST"],
    allow_headers=["*"],
)

# ---------------------------------------------------------------------------
# Middleware — log every request with method, path, status, and latency
# ---------------------------------------------------------------------------

@app.middleware("http")
async def log_requests(request: Request, call_next):
    start = time.perf_counter()
    response = await call_next(request)
    ms = (time.perf_counter() - start) * 1000
    _counters["total"] += 1
    logger.info(
        "%s %s → %d  (%.1f ms)",
        request.method, request.url.path, response.status_code, ms,
    )
    return response

# ---------------------------------------------------------------------------
# Endpoints
# ---------------------------------------------------------------------------

@app.get("/health", summary="Liveness check")
def health():
    """Returns 200 if the process is running."""
    return {"status": "ok", "spots_loaded": len(rec.df) if rec.df is not None else 0}


@app.get("/ready", summary="Readiness check")
def ready():
    """Returns 200 only if the model is fully loaded and can serve requests."""
    if rec.df is None or rec.Xw is None:
        raise HTTPException(status_code=503, detail="Model not yet loaded")
    try:
        test_prefs = UserPrefs()
        rec.recommend(test_prefs)
    except Exception as e:
        logger.error("Readiness check failed: %s", e)
        raise HTTPException(status_code=503, detail=f"Model check failed: {e}")
    return {"status": "ready", "spots_loaded": len(rec.df)}


@app.get("/stats", response_model=StatsResponse, summary="Usage statistics")
def stats():
    """Returns request counts and uptime since last server start."""
    return StatsResponse(
        uptime_seconds=round(time.time() - _start_time, 1),
        total_requests=_counters["total"],
        recommend_calls=_counters["recommend"],
        explain_calls=_counters["explain"],
        spots_calls=_counters["spots"],
        spots_loaded=len(rec.df) if rec.df is not None else 0,
    )


@app.get("/spots", summary="All study spots")
@limiter.limit("60/minute")
def get_spots(request: Request):
    """Return all 30 spots with full display metadata."""
    _counters["spots"] += 1
    return rec.all_spots()


@app.post("/recommend", response_model=RecommendResponse, summary="Get recommendations")
@limiter.limit("30/minute")
def recommend(prefs: UserPrefs, request: Request):
    """
    Return top-N study spot recommendations ranked by cosine similarity
    in the RF-weighted feature space.

    All fields are optional — omitting them applies sensible defaults.
    """
    _counters["recommend"] += 1
    t0 = time.perf_counter()
    try:
        results, query_vector = rec.recommend(prefs)
    except Exception as e:
        logger.error("Recommendation failed: %s", e)
        raise HTTPException(status_code=500, detail="Recommendation error")
    latency_ms = round((time.perf_counter() - t0) * 1000, 2)
    logger.info(
        "recommend | n=%d noise_tol=%d outdoor=%d quiet=%d → top=%s (%.1f ms)",
        prefs.n_results, prefs.noise_tolerance, prefs.wants_outdoor,
        prefs.wants_quiet, results[0]["name"] if results else "none", latency_ms,
    )
    return RecommendResponse(
        results=results, query_vector=query_vector, latency_ms=latency_ms
    )


@app.post("/recommend/explain", response_model=ExplainResponse, summary="Explain a recommendation")
@limiter.limit("30/minute")
def explain(req: ExplainRequest, request: Request):
    """
    Return a per-feature contribution breakdown showing why a specific spot
    was recommended for a given set of user preferences.
    """
    _counters["explain"] += 1
    try:
        contribs = rec.explain(req.spot_name, req.prefs)
    except ValueError as e:
        raise HTTPException(status_code=404, detail=str(e))
    except Exception as e:
        logger.error("Explain failed for %r: %s", req.spot_name, e)
        raise HTTPException(status_code=500, detail="Explanation error")
    return ExplainResponse(spot_name=req.spot_name, contributions=contribs)