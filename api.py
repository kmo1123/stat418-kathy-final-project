class UserPrefs(BaseModel):
    wants_wifi:        int = Field(1, ge=0, le=1,  description="Needs WiFi (0/1)")
    wants_outlets:     int = Field(0, ge=0, le=1,  description="Needs outlets (0/1)")
    wants_coffee:      int = Field(0, ge=0, le=1,  description="Wants coffee (0/1)")
    wants_food:        int = Field(0, ge=0, le=1,  description="Wants food (0/1)")
    wants_quiet:       int = Field(0, ge=0, le=1,  description="Needs quiet (0/1)")
    wants_outdoor:     int = Field(0, ge=0, le=1,  description="Prefers outdoor (0/1)")
    wants_group:       int = Field(0, ge=0, le=1,  description="Group study (0/1)")
    wants_comfortable: int = Field(0, ge=0, le=1,  description="Comfortable seating (0/1)")
    weather_ok:        int = Field(1, ge=0, le=1,  description="OK with weather risk (0/1)")
    noise_tolerance:   int = Field(2, ge=0, le=4,  description="Noise tolerance 0=silent 4=loud")
    connectivity_need: int = Field(1, ge=0, le=3,  description="Connectivity priority 0-3")
    n_results:         int = Field(3, ge=1, le=10, description="How many results to return")


